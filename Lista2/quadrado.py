ladoA = float(input("Digite o valor do primeiro lado: "))
ladoB = float(input("Digite o valor do segundo lado: "))
ladoC = float(input("Digite o valor do terceiro lado: "))
ladoD = float(input("Digite o valor do quarto lado: "))

if ladoA == ladoB == ladoC == ladoD:
    print("Os lados dados formam um quadrado perfeito.")
else:
    print("O quadrilátero não é um quadrado.")
